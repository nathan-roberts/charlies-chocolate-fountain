		<h3><a name="Q1" />How does the chocolate fountain work?</h3>

		<p>The chocolate is heated at the base of the fountain and pulled up by a specially made stainless steel corkscrew which rotates and brings the chocolate to the opening at the top of the fountain, then it overflows back down to the basin.</p>

		<h3><a name="Q2" />What kind of chocolate is used?</h3>

		<p>The chocolate fountain will operate with most brands of "coverture" chocolate. Coverture chocolate is a high quality chocolate that contains extra cocoa butter (32-39%). We only use the finest Belgian Chocolate made by Callebaut - available in Dark, Milk or white chocolate.</p>

